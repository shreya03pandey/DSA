class Solution{
    //Function to add two numbers represented by linked list.
    static Node addTwoLists(Node first, Node second){
        // code here
        // return head of sum list
        Node f=reverse(first);
        Node s=reverse(second);
        Node dummy=new Node(-1);
        Node head=dummy;
        int c=0;
        while(f!=null||s!=null||c!=0)
        {
          int res=c;
          if(f!=null)
          {
              res+=f.data;
              f=f.next;
          }
          if(s!=null)
          {
              res+=s.data;
              s=s.next;
          }
           c=res/10;
          Node t=new Node(res%10);
          dummy.next=t;
          dummy=dummy.next;
        }
        return reverse(head.next);
         
    }
    static Node reverse(Node head)
    {
        if(head==null)
        return head;
        Node curr=head;
        Node prev=null;
        while(curr!=null)
        {
            Node temp=curr.next;
            curr.next=prev;
            prev=curr;
            curr=temp;
        }
        return prev;
    }
    
}