class Solution {
    // Function to find the number of islands.
    public int numIslands(char[][] grid) {
        // Code here
        int n=grid.length;
        int m=grid[0].length;
        int vis[][]=new int[n][m];
        int cnt=0;
        for(int i=0;i<n;i++)
        {
            for(int j=0;j<m;j++)
            {
                if(grid[i][j]=='1'&& vis[i][j]==0)
                {
                dfs(grid,vis,i,j,n,m);
                cnt++;
                }
            }
        }
        return cnt;
    }
    public void dfs(char[][] grid,int[][] vis,int a,int b,int n,int m)
    {
        vis[a][b]=1;
        int dx[]={-1,-1,-1,0,0,1,1,1};
        int dy[]={-1,0,1,-1,1,-1,0,1};
        for(int i=0;i<8;i++)
        {
            int x=a+dx[i];
            int y=b+dy[i];
            if(x>=0&&x<n&&y>=0&&y<m&&grid[x][y]=='1'&&vis[x][y]==0)
            dfs(grid,vis,x,y,n,m);
        }
    }
}